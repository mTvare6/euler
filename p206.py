import sys
i = 101010101
while True:
    i+=1
    s = (i*i)
    if (s%10==9) and \
       (s//10**2 % 10 == 8) and \
       (s//10**4 % 10 == 7) and \
       (s//10**6 % 10 == 6) and \
       (s//10**8 % 10 == 5) and \
       (s//10**10 % 10 == 4) and \
       (s//10**12 % 10 == 3) and \
       (s//10**14 % 10 == 2) and \
        (s//10**16 % 10 == 1):
            print(i*10)
            sys.exit(0)
